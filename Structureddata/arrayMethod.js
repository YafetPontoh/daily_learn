// Reverse -> Untuk mengembalikan nilai attribute dari array tersebut
const myApp = ['Dicoding', 'Google', 'Translate']
myApp.reverse();
console.log(myApp); // Output: ['Translate', 'Google', 'Dicoding']

// Sort -> Untuk mengurutkan nilai Array 1-9A-Za-z)
const val = ['talia', 1, 'Andi', 'Firman', 'Amin', 'pensil', 'Ammar'];
val.sort();
console.log(val);  // Output: [ 1, "Amin", "Ammar", "Andi", "Firman", "pensil", "talia" ]
 
